public class BigSib {
    private String helloMsg;
    public BigSib() {
    helloMsg = "REGULAR_HUMAN_GREETING1.EXE";
    }
    public BigSib(String args) {
        helloMsg = args;
    }
    public String greet(String Sibname) {
        String retStr;
        retStr = helloMsg + " " + Sibname;
        return retStr;
    }
}